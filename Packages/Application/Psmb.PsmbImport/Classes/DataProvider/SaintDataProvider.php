      }

      if (!file_exists($filename)) {
        $html = file_get_contents($link);
        $html = mb_convert_encoding($html, "utf-8", 'windows-1251');
        $html = str_replace('windows-1251', 'utf-8', $html);
        file_put_contents($filename, $html);
      }
      $html = file_get_contents($filename);

      $dom = new \DomDocument();
      @$dom->loadHTML($html);
      $finder = new \DomXPath($dom);

      $headerNode = $finder->query("//*[contains(@class, 'header12')]")[0];
      $header = trim($headerNode->textContent);

      $imgNode = $dom->getElementsByTagName('img')[0];
      $iconFileName = trim($imgNode ? $imgNode->getAttribute('src') : null);
      $iconLocalFileName = null;
      if ($iconFileName) {
        $parentFolder = basename(dirname($link));
        $iconUrl = $imgNode ? dirname($link) . '/' . $iconFileName : "";
        if (strpos($iconFileName, '../') !== false) {
          $iconLocalFileName = $folder . '/' . str_replace('../', '', $iconFileName);
        } else {
          $iconLocalFileName = $folder . '/' . $parentFolder . $iconFileName;
        }


        if (!file_exists($iconLocalFileName)) {
          $imageFolder = pathinfo($iconLocalFileName, PATHINFO_DIRNAME);
          if (!file_exists($imageFolder)) {
            mkdir($imageFolder, 0777, true);
          }
          file_put_contents($iconLocalFileName, fopen($iconUrl, 'r'));
        }
      }

      $bodyNodes = $finder->query("//*[contains(@class, 'body10')]");
      $bodytext = array_reduce(iterator_to_array($bodyNodes), function ($r, $p) {
        return $r . "<p>" . innerHTML($p) . "</p>";
      }, "");

      $result[] = [
        '__externalIdentifier' => $key,
        'originalLink' => $link,
        'title' => StringValue::create($header)->getValue(),
        'icon' => StringValue::create($iconLocalFileName)->getValue(),
        'bodytext' => trim($bodytext)
      ];
    }

    $this->count = count($result);
    return $result;
  }
